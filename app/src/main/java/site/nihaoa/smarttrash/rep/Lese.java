package site.nihaoa.smarttrash.rep;

public class Lese {
    private Integer id;
    private Float lon;
    private Float lat;
    private Float baidu_lon;
    private Float baidu_lat;
    private Integer full;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Float getLon() {
        return lon;
    }

    public void setLon(Float lon) {
        this.lon = lon;
    }

    public Float getLat() {
        return lat;
    }

    public void setLat(Float lat) {
        this.lat = lat;
    }

    public Float getBaidu_lon() {
        return baidu_lon;
    }

    public void setBaidu_lon(Float baidu_lon) {
        this.baidu_lon = baidu_lon;
    }

    public Float getBaidu_lat() {
        return baidu_lat;
    }

    public void setBaidu_lat(Float baidu_lat) {
        this.baidu_lat = baidu_lat;
    }

    public Integer getFull() {
        return full;
    }

    public void setFull(Integer full) {
        this.full = full;
    }
}
